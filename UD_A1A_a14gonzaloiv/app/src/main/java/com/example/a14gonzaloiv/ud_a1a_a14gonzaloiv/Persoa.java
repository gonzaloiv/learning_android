public class Persoa {

    private String nome;
    private String descricion;

    public Persoa(String nome, String descricion){
        this.nome=nome;
        this.descricion=descricion;
    }

    public String getNome(){
        return nome;
    }

    public String getDescricion(){
        return descricion;
    }

}
